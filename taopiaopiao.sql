  performer VARCHAR(32),  #演员
  tag  VARCHAR(64),       #下面的标签
  --  下面是详情页的信息 
  video VARCHAR(128),     #短片
  pic_label VARCHAR(32),  #图片label 2D IMAX
  eng_name VARCHAR(32),   #英文名字
  label VARCHAR(64),      # 灾难 / 动作 / 剧情 
  show_time VARCHAR(64),  # 上映时间
  --  wish                #这一项在上面
  -- score评价在上面
  score_num VARCHAR(16),  # 评价人数
  intro VARCHAR(1024),    # 简介
  infor VARCHAR(64)       #110分钟 | 动画 | 吕艳婷 囧森瑟夫 瀚墨
  -- 评分分布表，影评和更多就省略了
