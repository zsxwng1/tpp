/** 电影的详细信息 **/
-- 其中  1：正在热映   用show来获取对应的电影
--       2：即将上映 
CREATE TABLE tpp_movie(
  mid INT PRIMARY KEY AUTO_INCREMENT,  #电影编号
  mname VARCHAR(32),      #电影名字
  d23 VARCHAR(16),        #2d or 3d
  screen VARCHAR(16),     #IMAX or 中国巨幕 
  pic  VARCHAR(128),      #电影图片
  is_show VARCHAR(16),    #正在热映  即将上映
