-- 通过这张表格查看有哪些影院符合 地址，电影 两个要求
CREATE TABLE tpp_cinema(
  cid INT PRIMARY KEY AUTO_INCREMENT,   #电影院编号 该影院的唯一编号      
  cname VARCHAR(32),      #电影院名字
  caddress VARCHAR(128),   #地址
  city_id VARCHAR(16),    #电影院所在城市编号
  city VARCHAR(32),       #电影院所在城市（杭州，上海..）
  tag VARCHAR(64),        #标签
  prefe VARCHAR(64),      #特惠信息
  lowest DECIMAL(10,1)    #41.7   元起
  -- 评分还是不写了
);


-- 通具体到某个影院的信息  某个电影院，具体的电影播放时间
CREATE TABLE tpp_cinema_movies(
  cmid INT PRIMARY KEY AUTO_INCREMENT,  #编号
  cid  INT ,              #电影院编号
  cname VARCHAR(32),      #电影院名字    #用来查找这种表格
  mid VARCHAR(16),        #电影编号
  -- day  日期复杂，不写
  time_start VARCHAR(16), #开始时间
  time_end VARCHAR(16),   #结束时间
  d23 VARCHAR(16),        #语言  2D
  room VARCHAR(16),       #影厅
  price_e DECIMAL(10,1),  #特惠价 初始价*0.9
  price_s DECIMAL(10,1)   #初始价 
  -- 其他，包括（开始时间，日期，影院什么的，都编造）
);

-- 电影院有哪些电影   这里控制着电影院-电影的排列顺序
CREATE TABLE tpp_cinema_hasmovies(
  cmid INT PRIMARY KEY AUTO_INCREMENT,  #编号
  cid  INT ,              #电影院编号
  city_id  INT,           #城市编号
  mname VARCHAR(32),      #电影名字    #用来查找这种表格
  mid VARCHAR(16)         #电影编号
);

 
