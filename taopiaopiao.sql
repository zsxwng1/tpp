/** 用户信息 :**/
/** 保存了uname,uphone,upwd  **/
--  注册时输入下面3项，即可完成注册
CREATE TABLE tpp_user(
  uid INT PRIMARY KEY AUTO_INCREMENT,
  uname VARCHAR(32),   #3-8位用户名
  uphone VARCHAR(16),  #11位手机号码 
  upwd VARCHAR(32)     #6-18位大写小字母，数字
);

